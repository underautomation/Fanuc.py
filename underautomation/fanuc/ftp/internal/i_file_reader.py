import typing
from UnderAutomation.Fanuc.Ftp.Internal import IFileReader as i_file_reader

class IFileReader:
	'''Interface for Fanuc file readers'''
	def __init__(self, _internal = 0):
		if(_internal == 0):
			self._instance = i_file_reader()
		else:
			self._instance = _internal

	@property
	def file_name(self) -> str:
		'''File name'''
		return self._instance.FileName

	def __str__(self):
		return self._instance.ToString() if self._instance is not None else ""

	def __repr__(self):
		return self.__str__()

	def __eq__(self, other) -> bool:
		if not isinstance(other, IFileReader):
			NotImplemented
		return self._instance.Equals(other._instance)

	def __hash__(self) -> int:
		return self._instance.GetHashCode() if self._instance is not None else 0
